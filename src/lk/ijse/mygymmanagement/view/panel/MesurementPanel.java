    private void loadMemberIDs() {
        try {
            ArrayList<MemeberDTO> memberList = MemberController.getAllMember();
            for (MemeberDTO memeberDTO : memberList) {
                cmbMemberID.addItem(memeberDTO.getMemberID());
            }

        } catch (Exception ex) {
            //JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(MesurementPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadScheduleIDs() {
        try {
            ArrayList<ScheduleDTO> scheduleList = ScheduleController.getAllSchedule();
            for (ScheduleDTO scheduleDTO : scheduleList) {
                cmbScheduleID.addItem(scheduleDTO.getScheduleID());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
           // Logger.getLogger(MesurementPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void getMeasurementID() {
        String newID;
        try {
            newID = IDGenarator.getNewID("measurement", "Measurement_ID", "Mes");
            txtMeasurmentID.setText(newID);

        } catch (SQLException ex) {
            Logger.getLogger(MesurementPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MesurementPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


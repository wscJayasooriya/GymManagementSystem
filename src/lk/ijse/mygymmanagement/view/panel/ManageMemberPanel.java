    private void lblModifyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblModifyMouseClicked
        if (cmbGender.getSelectedIndex() == -1) {
            return;
        }

        MemeberDTO member = new MemeberDTO(
                txtMemberID.getText(),
                txtMemberName.getText(),
                txtNIC.getText(),
                txtOccupation.getText(),
                txtAddress.getText(),
                txtDOB.getText(),
                Integer.parseInt(txtAge.getText()),
                cmbGender.getSelectedItem().toString(),
                Double.parseDouble(txtHeight.getText()),
                Double.parseDouble(txtWeight.getText()),
                txtEmail.getText(),
                txtPhone.getText(),
                txtEmrContact.getText()
        );
        try {
            boolean result = MemberController.updateMember(member);

            if (result) {
                JOptionPane.showMessageDialog(this, "Member has been successfully Updated.");
                clearAllTextFileds();
            } else {
                JOptionPane.showMessageDialog(this, "Member hasn't been Updated due to some error");

            }

        } catch (Exception ex) {
            Logger.getLogger(ManageMemberPanel.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
        txtMemberID.requestFocus();
    }//GEN-LAST:event_lblModifyMouseClicked


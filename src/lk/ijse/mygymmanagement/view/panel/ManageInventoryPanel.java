    private void lblSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSaveMouseClicked
        
        try {
            InventoryDTO inventory = new InventoryDTO(txtInventoryID.getText(),
                    txtInventoryName.getText(),
                    txtBrandName.getText(),
                    Integer.parseInt(txtQty.getText()));
            boolean result = ManageInventoryController.addInventory(inventory);
            
            if (result) {
                JOptionPane.showMessageDialog(this, "Inventory has been successfully Saved.");
                loadAllInventory();
                getInventoryID();
                lblSave.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Inventory hasn't been Saved due to some error");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        clearAllTextFileds();
        txtInventoryName.requestFocus();
    }//GEN-LAST:event_lblSaveMouseClicked

    private void lblRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblRemoveMouseClicked
        InventoryDTO inventory = new InventoryDTO(txtInventoryID.getText(),
                txtInventoryName.getText(),
                txtBrandName.getText(),
                Integer.parseInt(txtQty.getText()));
        try {
            boolean result = ManageInventoryController.deleteInventory(inventory);
            
            if (result) {
                JOptionPane.showMessageDialog(this, "Inventory has been successfully deleted.");
                loadAllInventory();
            } else {
                JOptionPane.showMessageDialog(this, "Inventory hasn't been deleted due to some error");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ManageInventoryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        clearAllTextFileds();
        txtInventoryID.requestFocus();
    }//GEN-LAST:event_lblRemoveMouseClicked

    private void lblModifyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblModifyMouseClicked
        try {
            InventoryDTO inventory = new InventoryDTO(
                    txtInventoryID.getText(),
                    txtInventoryName.getText(),
                    txtBrandName.getText(),
                    Integer.parseInt(txtQty.getText())
            );
            
            boolean updateInventory = ManageInventoryController.updateInventory(inventory);
            
            if (updateInventory) {
                JOptionPane.showMessageDialog(this, "Modify Success..");
                loadAllInventory();
                clearAllTextFileds();
                txtInventoryID.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Modify Error..");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageInventoryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblModifyMouseClicked


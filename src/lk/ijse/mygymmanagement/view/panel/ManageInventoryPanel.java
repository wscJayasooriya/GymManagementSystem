    private void checkText() {
        String text = txtQty.getText();
        String brand = txtBrandName.getText();
        String invtId = txtInventoryID.getText();
        String intName = txtInventoryName.getText();
        if ((text.equals("")) && (brand.equals("")) && (intName.equals("")) && (invtId.equals(""))) {
            lblSave.setEnabled(false);
        } else {
            lblSave.setEnabled(true);
        }
    }
    
    private void getInventoryID() {
        String newID;
        try {
            newID = IDGenarator.getNewID("inventory","Inventory_ID", "Inv");
            txtInventoryID.setText(newID);

        } catch (SQLException ex) {
            Logger.getLogger(RegMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

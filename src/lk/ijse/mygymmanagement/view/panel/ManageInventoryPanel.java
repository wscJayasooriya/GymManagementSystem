    private void loadAllInventory() {
        try {
            ArrayList<InventoryDTO> allInventories = ManageInventoryController.getAllInventory();
            
            DefaultTableModel dtm = (DefaultTableModel) tblInventory.getModel();
            
            dtm.setRowCount(0);
            
            if (allInventories != null) {
                
                for (InventoryDTO inventory : allInventories) {
                    
                    Object[] rowData = {
                        inventory.getInventoryID(),
                        inventory.getInventoryName(),
                        inventory.getBrandName(),
                        inventory.getQty()
                    };
                    
                    dtm.addRow(rowData);
                    
                }
                
            }
            lblCount.setText(Integer.toString(tblInventory.getRowCount()));
            
        } catch (Exception ex) {
            Logger.getLogger(ManageInventoryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

    private void checkText() {
        String id = txtInstructorID.getText();
        String name = txtInstructorName.getText();
        String tele = txtInstructorTele.getText();
        String nic = txtInstructorNIC.getText();
        String address = txtInstructorAddress.getText();

        if ((id.equals("")) && (name.equals("")) && (tele.equals("")) && (nic.equals("")) && (address.equals(""))) {
            lblSave.setEnabled(false);
        } else {
            lblSave.setEnabled(true);
        }
    }
    
    private void getInstructorID() {
        String newID;
        try {
            newID = IDGenarator.getNewID("instructor","Instructor_ID", "Ins");
            txtInstructorID.setText(newID);

        } catch (SQLException ex) {
            Logger.getLogger(RegMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

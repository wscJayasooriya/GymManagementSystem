    private void lblSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSaveMouseClicked
        try {
            InstructorDTO instructor = new InstructorDTO(txtInstructorID.getText(),
                    txtInstructorName.getText(),
                    txtInstructorTele.getText(),
                    txtInstructorNIC.getText(),
                    txtInstructorAddress.getText(),
                    cmbGender.getSelectedItem().toString());

            boolean result = ManageInstructorController.addInstructor(instructor);

            if (result) {
                JOptionPane.showMessageDialog(this, "Instructor has been successfully Saved.");
                loadAllInstructors();
                getInstructorID();
                
                lblSave.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Instructor hasn't been Saved due to some error");
                lblSave.setEnabled(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Fill All Fields");
        }
        clearAllTextFileds();
        txtInstructorName.requestFocus();
    }//GEN-LAST:event_lblSaveMouseClicked


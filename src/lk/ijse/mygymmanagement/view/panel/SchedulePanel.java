    private void lblViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblViewMouseClicked

        QueryDAO queryDAO = (QueryDAO) DAOFactory.getInstance().getDAO(DAOFactory.DAOTypes.QUERY);

        try {
            ArrayList<ScheduleInfoDTO> scheduleInfo = queryDAO.getScheduleInfo(cmbMemberRegNo.getSelectedItem().toString());

            DefaultTableModel dtm = (DefaultTableModel) tblSchedule.getModel();

            dtm.setRowCount(0);

            if (scheduleInfo != null) {

                for (ScheduleInfoDTO schedule : scheduleInfo) {

                    Object[] rowData = {
                        schedule.getScheduleID(),
                        schedule.getExerciseID(),
                        schedule.getExerciseName(),
                        schedule.getStartDate(),
                        schedule.getEndDate(),
                        schedule.getSets(),
                        schedule.getRep()
                    };

                    dtm.addRow(rowData);

                }

            }

        } catch (Exception ex) {
            Logger.getLogger(SchedulePanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_lblViewMouseClicked


    private void lblSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSaveMouseClicked
        try {
            ScheduleDTO schedule = new ScheduleDTO(
                    txtScheduleID.getText(),
                    cmbMemberRegNo.getSelectedItem().toString(),
                    cmbInstructorID.getSelectedItem().toString(),
                    txtStartDate.getText(),
                    txtEndDate.getText()
            );

            ScheduleDetailDTO sDetail = new ScheduleDetailDTO(
                    txtScheduledetailID.getText(),
                    txtScheduleID.getText(),
                    cmbExerciseID.getSelectedItem().toString(),
                    txtSet.getText(),
                    txtRep.getText()
            );

            boolean result = ScheduleDetailController.addScheduleDetail(sDetail, schedule);

            if (result) {
                JOptionPane.showMessageDialog(this, "Schedule has been successfully Created.");
                clearTextFields();
                getScheduleDetailID();
                // getScheduleID();
            } else {
                JOptionPane.showMessageDialog(this, "Schedule hasn't been Created due to some error");
            }
        } catch (Exception ex) {
            Logger.getLogger(RegMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbMemberRegNo.requestFocus();

    }//GEN-LAST:event_lblSaveMouseClicked


